module.exports = {
    upper:    { args: 1, returns: 1, fn: ([s]) => [s.toUpperCase()] },
    lower:    { args: 1, returns: 1, fn: ([s]) => [s.toLowerCase()] },
    len:      { args: 1, returns: 1, fn: ([s]) => [String(s.length)] },
    trim:     { args: 1, returns: 1, fn: ([s]) => [s.trim()] },
    triml:    { args: 1, returns: 1, fn: ([s]) => [s.trimStart()] },
    trimr:    { args: 1, returns: 1, fn: ([s]) => [s.trimEnd()] },
    reverse:  { args: 1, returns: 1, fn: ([s]) => [[...s].reverse().join('')] },
    includes: { args: 2, returns: 1, fn: ([s, sub])       => [String(s.includes(sub))] },
    starts:   { args: 2, returns: 1, fn: ([s, pre])       => [String(s.startsWith(pre))] },
    ends:     { args: 2, returns: 1, fn: ([s, suf])       => [String(s.endsWith(suf))] },
    index:    { args: 2, returns: 1, fn: ([s, sub])       => [String(s.indexOf(sub))] },
    slice:    { args: 3, returns: 1, fn: ([s, a, b])      => [s.slice(Number(a), Number(b))] },
    replace:  { args: 3, returns: 1, fn: ([s, from, to])  => [s.replaceAll(from, to)] },
    split:    { args: 2, returns: 1, fn: ([s, sep])       => [s.split(sep).join('\x1f')] },
    pad:      { args: 3, returns: 1, fn: ([s, n, ch])     => [s.padStart(Number(n), ch || ' ')] },
    padr:     { args: 3, returns: 1, fn: ([s, n, ch])     => [s.padEnd(Number(n), ch || ' ')] },
    repeat:   { args: 2, returns: 1, fn: ([s, n])         => [s.repeat(Number(n))] },
    at:       { args: 2, returns: 1, fn: ([s, i])         => [s[Number(i)] ?? ''] },
    code:     { args: 1, returns: 1, fn: ([s])            => [String(s.charCodeAt(0))] },
    fromcode: { args: 1, returns: 1, fn: ([n])            => [String.fromCharCode(Number(n))] },
};
